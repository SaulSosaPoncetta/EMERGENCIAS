package Modelo;

public class Usuarios {
    private int idUsuario;
    private String NumeroLegajoUsuario;
    private String Password;
    private String NombreUsuario;
    private String CorreoElectronico;
    private String UltimaSession;
    private int idTipoUsuario;
    private String TipoUsuario;

    public int getIdUsuario() {
        return idUsuario;
    }

    public void setIdUsuario(int idUsuario) {
        this.idUsuario = idUsuario;
    }

    public String getNumeroLegajoUsuario() {
        return NumeroLegajoUsuario;
    }

    public void setNumeroLegajoUsuario(String NumeroLegajoUsuario) {
        this.NumeroLegajoUsuario = NumeroLegajoUsuario;
    }

    public String getPassword() {
        return Password;
    }

    public void setPassword(String Password) {
        this.Password = Password;
    }

    public String getNombreUsuario() {
        return NombreUsuario;
    }

    public void setNombreUsuario(String NombreUsuario) {
        this.NombreUsuario = NombreUsuario;
    }

    public String getCorreoElectronico() {
        return CorreoElectronico;
    }

    public void setCorreoElectronico(String CorreoElectronico) {
        this.CorreoElectronico = CorreoElectronico;
    }

    public String getUltimaSession() {
        return UltimaSession;
    }

    public void setUltimaSession(String UltimaSession) {
        this.UltimaSession = UltimaSession;
    }

    public int getIdTipoUsuario() {
        return idTipoUsuario;
    }

    public void setIdTipoUsuario(int idTipoUsuario) {
        this.idTipoUsuario = idTipoUsuario;
    }

    public String getTipoUsuario() {
        return TipoUsuario;
    }

    public void setTipoUsuario(String TipoUsuario) {
        this.TipoUsuario = TipoUsuario;
    }

    
}
