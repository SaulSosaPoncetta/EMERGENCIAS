    private void btnIngresarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIngresarActionPerformed
        SqlUsuarios modSql = new SqlUsuarios();
        Usuarios mod = new Usuarios();
        
        //Tomaremos la hs del sistema para archivar el Login.OJO Agregar libreria de java.util.date no la de SQL.
        Date date = new Date();
        DateFormat fechaHora = new SimpleDateFormat("yyyy-MM-dd HH:mm:ss");
        
        
        String pass = new String(txtPassword.getPassword());
        
        if(!txtNumeroLegajo.getText().equals("") && !pass.equals(""))
        {
            String nuevoPass = Hash.sha1(pass);
            
            //Envio el modelo para validar identidad.
            mod.setNumeroLegajo(txtNumeroLegajo.getText());
            mod.setPassword(nuevoPass);
            mod.setUltimaSession(fechaHora.format(date));
            
            
            if(modSql.login(mod))
            {
               
                Inicio.frmLog = null;
                this.dispose();
                          
                                
                frmPrincipal = new PrincipalEmergencias();
                frmPrincipal.setVisible(true);
                
                JOptionPane.showMessageDialog(null, "ejecutando principal");
                
            } else {
                JOptionPane.showMessageDialog(null, "DATOS DE LOGIN INCORRECTOS, VERIFIQUE Y VUELVA A INGRESARLOS");
            }
            
        } else {
            JOptionPane.showMessageDialog(null, "DATOS DE LOGIN INCOMPLETOS, VERIFIQUE Y VUELVA A INGRESARLOS");
        }
        
    }//GEN-LAST:event_btnIngresarActionPerformed

